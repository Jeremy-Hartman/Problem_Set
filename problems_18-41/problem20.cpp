//Problem 20 - Solution


#include<iostream>

using namespace std;

int main()
{
	int x;

	cout<<"Please enter number: ";
	cin>>x;

	cout<<"Counting down"<<endl;

	for(int i = x; i>=1; i--)	// if i is greater than 1 decrement i by 1
	{
		cout<<"Counter is "<<i<<endl;	// if above statement true cout<<"Counter is "<<i<<endl;
	}
	cout<<endl;

	return 0;
}





